// DTCG 토큰 -> Kotlin(Compose) 소스 생성. hm()/HmTextStyle 헬퍼는
// android/src/main/kotlin/halfmoon/tokens/Support.kt(수기)에 있다.
// 색상은 semantic만 노출 — swift 포맷과 같은 "primitive 직접 참조 금지" 원칙.

// ponytail: alpha FF 고정 (현재 토큰 전부 불투명). 반투명 토큰이 생기면 alpha 파라미터 추가.
const hex = (v) => '0x' + v.hex.slice(1).toUpperCase();

const camel = (parts) =>
  parts.map((p, i) => (i === 0 ? p : p[0].toUpperCase() + p.slice(1))).join('');

// Kotlin 식별자는 숫자로 시작 못 함: "4" -> "_4", "2xl" -> "_2xl"
const ident = (name) => (/^\d/.test(name) ? '_' + name : name);

const num = (v) => (typeof v === 'object' ? v.value : v);

export function generateKotlin(lightAll, darkAll) {
  const val = (t) => t.$value ?? t.value;
  const type = (t) => t.$type ?? t.type;
  const dark = new Map(darkAll.map((t) => [t.path.join('.'), val(t)]));

  const colors = [];
  const groups = { space: [], radius: [], fontSize: [], fontWeight: [], lineHeight: [], typography: [] };

  for (const t of lightAll) {
    const p = t.path;
    const v = val(t);
    if (type(t) === 'color' && t.filePath.includes('/semantic/')) {
      const name = camel(p.slice(1));
      // dark 소스는 light 위에 덮어쓴 것이라 모든 경로가 존재한다
      colors.push(
        `        val ${name}: Color @Composable @ReadOnlyComposable get() = hm(light = ${hex(v)}, dark = ${hex(dark.get(p.join('.')))})`,
      );
    } else if (p[0] === 'space' || p[0] === 'radius') {
      groups[p[0]].push(`        val ${ident(camel(p.slice(1)))}: Dp = ${num(v)}.dp`);
    } else if (p[0] === 'size' && p[1] === 'font') {
      groups.fontSize.push(`        val ${ident(camel(p.slice(2)))}: TextUnit = ${num(v)}.sp`);
    } else if (p[0] === 'font' && p[1] === 'weight') {
      groups.fontWeight.push(`        val ${camel(p.slice(2))}: FontWeight = FontWeight(${v})`);
    } else if (p[0] === 'font' && p[1] === 'lineHeight') {
      groups.lineHeight.push(`        val ${camel(p.slice(2))}: Float = ${v}f`);
    } else if (p[0] === 'typography') {
      groups.typography.push(
        `        val ${camel(p.slice(1))} = HmTextStyle(size = ${num(v.fontSize)}f, weight = ${v.fontWeight}, lineHeight = ${v.lineHeight}f)`,
      );
    }
    // font.family는 스킵 — Pretendard 번들링 전까지 시스템 폰트 사용 (Support.kt 참조)
  }

  const block = (name, lines) => `    object ${name} {\n${lines.join('\n')}\n    }`;
  return `// Generated by packages/tokens/build/build.mjs — DO NOT EDIT.
package halfmoon.tokens

import androidx.compose.runtime.Composable
import androidx.compose.runtime.ReadOnlyComposable
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.text.font.FontWeight
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.TextUnit
import androidx.compose.ui.unit.dp
import androidx.compose.ui.unit.sp

object HM {
${[block('color', colors), ...Object.entries(groups).map(([n, l]) => block(n, l))].join('\n\n')}
}
`;
}
